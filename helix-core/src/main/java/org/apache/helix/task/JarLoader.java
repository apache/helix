import java.net.URL;

/**
 * The interface that is to be implemented by a specific JAR loader.
 */
public interface JarLoader {
  /**
   * Opens a JAR file and returns its URL.
   * @param jar path of the JAR file
   * @return URL of the file at path jar
   */
  URL openJar(String jar);
}
